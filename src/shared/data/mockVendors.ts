import type {
  Vendor,
  VendorActivity,
  VendorBill,
  VendorDocument,
  VendorPayment,
  VendorServiceMapping,
} from '@/shared/types/vendor'

function daysAgo(days: number) {
  const d = new Date()
  d.setDate(d.getDate() - days)
  return d.toISOString()
}

function mapping(
  id: string,
  serviceMasterId: string,
  vendorRate: number,
  clientBillingRate: number,
  gstApplicable = true,
): VendorServiceMapping {
  return {
    id,
    serviceMasterId,
    vendorRate,
    clientBillingRate,
    margin: clientBillingRate - vendorRate,
    gstApplicable,
    status: 'active',
  }
}

function doc(
  id: string,
  documentName: string,
  documentType: VendorDocument['documentType'],
  fileName: string,
  days: number,
): VendorDocument {
  return {
    id,
    documentName,
    documentType,
    fileName,
    uploadedAt: daysAgo(days),
    uploadedBy: 'Rajan Mehta',
  }
}

function bill(
  id: string,
  invoiceNumber: string,
  amount: number,
  status: VendorBill['status'],
  daysInvoice: number,
  daysDue: number,
): VendorBill {
  const paidAmount =
    status === 'paid' ? amount : status === 'partially_paid' ? Math.round(amount * 0.6) : 0
  return {
    id,
    invoiceNumber,
    invoiceDate: daysAgo(daysInvoice).slice(0, 10),
    invoiceAmount: amount,
    dueDate: daysAgo(daysDue).slice(0, 10),
    status,
    paidAmount,
  }
}

function payment(id: string, ref: string, amount: number, days: number, remarks: string): VendorPayment {
  return {
    id,
    paymentReference: ref,
    paymentDate: daysAgo(days).slice(0, 10),
    amount,
    remarks,
  }
}

function activities(entries: Omit<VendorActivity, 'id'>[]): VendorActivity[] {
  return entries.map((e, i) => ({ ...e, id: `vnd-act-${i}` }))
}

